package learningJavaOne;

public class looping {

	public static void main(String[] args) {
		// datatype, variable, conditional statmen
		
		// looping - repeating some code 
		
		
	// for or while loop 
		// intializtion, condition, increament or decreament 
		
		for(int x=1; x<15;  x++) {
			
			System.out.println(x);
			
			
		}
		
		System.out.println("The code is out of the loop");
		
		// iteration
		
		for(int y = 30; y>10; y--) {
			
			
			System.out.println("printing the value of y");	
			
			System.out.println(y);	
			
			
		}
		
		System.out.println("The code is out of the loop for the second time");	
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
